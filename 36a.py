class Solution:
    def isValidSudoku(self, board: list[list[str]]) -> bool:
        for i in range(0, 3):  # i * 3
            for j in range(0, 3):
                log_hor = set()

                for l in range(0, 3):
                    for k in range(0, 3):
                        num_hor = board[(i * 3) + l][(j * 3) + k]
                        if num_hor == ".":
                            continue

                        if num_hor in log_hor:
                            return False

                        log_hor.add(num_hor)

        for i in range(0, 9):
            log_hor = set()
            log_ver = set()

            for j in range(0, 9):
                num_hor = board[i][j]

                if num_hor != ".":
                    if num_hor in log_hor:
                        return False

                    log_hor.add(num_hor)

                num_ver = board[j][i]

                if num_ver != ".":
                    if num_ver in log_ver:
                        return False

                    log_ver.add(num_ver)

        return True


